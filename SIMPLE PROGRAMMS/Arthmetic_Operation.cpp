#include <iostream>
using namespace std;
int main()
{
    int A, B;
    cout << "ENTER TWO NUMBER: ";
    cin >> A >> B;
    cout << "The SUM is: " << A + B << endl;
    cout << "The SUB is: " << A - B << endl;
    cout << "The MUL is: " << A * B << endl;
    cout << "The DIV is: " << (float)A / B << endl;
    return 0;
}